bool oled_task_user(void) {
    if (is_keyboard_master()) {
        oled_render_layer_state();
        //oled_render_keylog();
    } else {
      if(prev_val >= 20){
        prev_val = 0;
        num_val++;
        if(num_val == 3){
          num_val = 0;
        }
      }

      if(num_val == 0){
        oled_render_logo();
      } else if(num_val == 1){
        oled_render_logo2();
      } else if(num_val == 2){
        oled_render_logo3();
      }

      prev_val = prev_val + 1;
    }
    return false;
}

